package f_recursion.H_demo.demo2;

public class Arr {
	private int index;
	private int num;

	public Arr(int num, int index) {
		this.index = index;
		this.num = num;
	}

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

}
